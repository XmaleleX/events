<h4 class="panel-title">
<a href="#mp14" data-toggle="collapse" data-parent="#mp" onclick="display_accord('#mp2');">Buying on credit</a>
</h4>
</div>
<div class="panel-collapse collapse" id="mp2">
<p class="panel-body">
<b class="b_heading">Buying on credit and credit agreements</b><br>
When people need to buy things like furniture, they often do not have enough money to pay cash. They can then decide to buy the things on account or on credit where they pay over a period of time or at a later date. When doing this they make debt for themselves.<br>
When people buy on credit they do this through a credit provider. Credit providers are organisations or people that lend money – such as banks and micro-lenders – or that sell goods on credit, such as shops.<br>
<b class="b_heading">Owing money and being in debt</b><br>
<b class="b_heading">Why people get into debt</b>
People borrow money for various of reasons, including paying for regular monthly expenses – like rent and transport, emergencies – like illness or death, occasional big expenses – like school fees and car maintenance, to buy furniture or a car or other big, expensive items, to start a small business or to pay back existing debts.<br>
When a person wants to borrow money it is important for them to understand that buying on credit costs more than paying cash. The amount that a person ends up repaying includes:<br>
<ul>
<li>Basic loan plus</li>
<li>Finance charges (includes interest on the loan, bank and administration charges) plus</li>
<li>Insurance charges (where this is applicable)</li>
</ul>
<b class="b_heading">If a person is considering buying on credit they must find out:</b>
<ul>
<li>What the total amount for the goods will be, including all the finance charges;</li>
<li>How much the deposit and monthly payments;</li>
<li>The period of time for paying off the loan.</li>
</ul>
The quicker a person pays off the debt, the lower the extra finance charges will be. The longer it takes to pay off, the more the goods end up costing.<br>
People get into difficulty paying back the money they have borrowed for all kinds of reasons. When someone does not make the monthly payment they agreed to when signing a contract, this is called defaulting. If a person is having trouble paying back their debts they can opt for help from recognised debt administration organisations and companies to plan and manage their finances better; and/or plan to pay off their debts.<br>
The consumer has the responsibility to inform the credit provider if they are unable to meet monthly repayments.<br>
The consumer has the right to go to their credit provider to make new arrangements or payments – although the credit provider does not have to agree to the consumer’s proposed arrangements<br>
</p>
