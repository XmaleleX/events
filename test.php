<li>You will need to provide some personal information – such as your ID number and address.</li>
</ul>
<b class="b_heading">How do you correct your Credit Record?</b>
<ul>
<li>If you disagree with your credit record, you must send in the corrections within 30 days – during this time, you can get additional free copies of your record to check the corrections have been done. After this, you must pay a small fee for a copy.</li>
<li>Contact the credit bureau and ask for a dispute form. They will send you the form and a reference number.</li>
<li>Complete the forms and send them back to the bureau with a copy of your ID.</li>
<li>Include proof of the information you want changed, such as a debt clearance certificate or statement of account.</li>
<li>If they have kept a negative listing longer than they should have, insist they remove the record from their system. They have 20 working days to investigate the matter.</li>
<li>Ask for another copy of your credit report to check that the changes have been made.</li>
</ul>
