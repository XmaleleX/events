<h4 class="panel-title">
<a href="#m_money4" data-toggle="collapse" data-parent="#m_money" onclick="display_accord(m_money1);">The importance of budgeting</a>
</h4>
</div>
<div class="panel-collapse collapse" id="m_money1">
<p class="panel-body">
<b class="b_heading">The importance of budgeting</b><br>
A budget is the most fundamental and most effective financial management tool available to anyone–yes, anyone, whether you are earning thousands of rands a year. It is extremely important to know how much money you have to spend and where you are spending it. Some of your “spending” might be for investments, but there is an important distinction between creating a personal budget and deciding where to invest your extra income. A budget is the first and most important step towards maximizing the power of your money.<br>
</p>
</div>
</div>
<hr class="faq_hr"/>
<div class="panel panel-success">
<div class="panel-heading">
<h4 class="panel-title">
<a href="#m_money4" data-toggle="collapse" data-parent="#m_money" onclick="display_accord(m_money2);">How to budget</a>
</h4>
</div>
<div class="panel-collapse collapse" id="m_money2">
<p class="panel-body">
If you’ve ever found yourself wondering where your money goes from one week to the next, getting on top of your finances should be as simple as starting with a budget
<ul>
<li><b class="b_heading">Know what is going on.</b><br>
Personal budgeting allows you to know exactly how much money you have-even down to the last cent. Furthermore, a budget is a self-education tool that shows you how your funds are allocated, how they are working for you, what your plans are for them, and how far along you are toward reaching your goals.</li>
<li><b>Control.</b><br>
A budget is the key to help you to take charge of your finances. With a budget, you have the tools to decide exactly what is going to happen to your hard-earned money-and when. You can be in control of your money, instead of letting it control you!</li>
<li><b>Organisation.</b><br>
Even in its simplest form, a budget follows a system that divides funds into categories of income, expenses and savings. Added to that, budgets can provide further organisation by automatically providing records of all your monetary transactions and a simple filing system to organise bills, receipts, and financial statements.</li>
<li><b>Communication.</b><br>
If you are married, have a family, or share money with anyone, having a budget that you all create together is helpful in resolving personal differences about the way your money will be handled. The budget is a communication tool to discuss the priorities for where your money should be spent, as well as allowing all involved parties to “run” the system.</li>
</ul>
</p>
</div>
</div>
<hr class="faq_hr"/>
<div class="panel panel-success">
<div class="panel-heading">
<h4 class="panel-title">
<a href="#m_money4" data-toggle="collapse" data-parent="#m_money" onclick="display_accord(m_money3);">What is a budget?</a>
</h4>
</div>
<div class="panel-collapse collapse" id="m_money3">
<p class="panel-body">
A budget is one of the most basic and probably most useful things you can do to get in control of your finances. It is simply a snapshot of your financial situation at a particular point in time, which can help you keep track of what you’re earning, what you’re spending, and what happens to the leftovers (if there are any). You can then use your budget to set some guidelines for yourself when it comes to how you spend what you earn.<br>
<b class="b_heading">Tips for making your budget work for you</b>
<small><b>Give yourself an allowance</b></small><br>
Some people want to be really strict with their spending (or they might have financial commitments that leave them with very little spending money or none at all). But, if you can, allocate yourself a small allowance – it will make your budget a little more flexible, which means you’re more likely to stick to it.<br>
<small><b>Don’t forget emergency money</b></small><br>
Try to allocate a certain amount from each pay to go towards emergencies and unexpected expenses. We’ve all had them at some time or another like an expensive visit to the doctor or your car that needs new brakes and tyres. If you’ve got something set aside to cover the occasional emergency (or at least help towards the bulk of the cost), then you’re less likely to find your budget being thrown off track if something unexpected occurs.<br>
<small><b>Set yourself some goals</b></small><br>
It’s useful to try and set a few financial goals in your budget. Having clear goals can help you decide what’s important and also gives you an incentive to keep your finances under control. These could include – saving up to go on holiday or planning for your child’s school fees next year etc. What do you want to work towards?<br>
<small><b>Regular reviews</b></small><br>
Last but not least, regularly check yourself against your budget. This will help you make sure you’re sticking to the plan, while also knowing when your budget is no longer meeting your needs. Events such as a pay rise or a reduction in take-home pay, a new addition to the family, or taking on a new loan – all of these will affect your finances and your budget will need to change in order to accommodate them.<br>
</p>
