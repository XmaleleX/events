<p class="panel-body">
<b class="b_heading">What is a Personal loan?</b><br>
A personal loan is one of the most basic types of loans. It can be used to fund anything – home improvements, furniture, a vacation, or an unforeseen expense. Some people use a personal loan to consolidate their debt.
<br>
A personal loan is a reliable option for anyone who needs access to immediate cash for personal use. There are two basic types of personal loans, secured and unsecured loans.<br>
<b class="b_heading">Secured Loan</b><br>
A secured loan requires you to offer one or more of your assets as a form of collateral. If you are unable to repay the loan, the lending institution will be able to take your assets for immediate repayment.
<br>
You will generally be offered a competitive interest rate if you take out a secured loan, because the risk to a lender is not as high as in the case of an unsecured loan.<br>
<b class="b_heading">Unsecured Loan</b><br>
An unsecured loan does not require that you provide any form of guarantee. Consequently, you will pay more interest on an unsecured loan because the risk to the lender is greater.
<br>
There are many banks and lending institutions that offer unsecured personal loans. These are specifically tailored for people who do not have any property or assets to offer as collateral.<br>
<b class="b_heading">Personal Loan Basics</b><br>
You can take out a personal loan for as much as you can afford depending on the rules of the lender. Monthly repayments are usually calculated according to your loan terms and your credit score.
<br>
Lenders are required to do a credit check to determine how much you can afford.<br>
<b class="b_heading">Personal Loan Lending Criteria</b><br>
Lenders will ask you to provide them with your most recent payslips, your South African identity document and your latest bank statement.
<br>
You may be required to take out credit life insurance on your loan in the case of retrenchment, disability or death.<br>
</p>
