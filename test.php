<p class="panel-body">

<b class="b_heading">What is a Credit Bureau?</b>
A Credit bureau is an organisation that keeps a record of your credit information. Your credit record shows how you manage your debts and is used by credit providers and moneylenders to decide if you can afford to borrow money or pay back a new loan.
The National Credit Act says each credit bureau must be registered with the National Credit Regulator – who decides how your credit information can be used and who can see your credit record.
<b class="b_heading">What are the responsibilities of a Credit Bureau?</b>
<ul>
<li>To file information on your credit record after the fee is paid by the credit provider.</li>
<li>To make sure the information is correct and not keep inaccurate information.</li>
<li>To only keep information for the prescribed period and to a prescribed standard.</li>
<li>To provide a report of the information – a credit report – when required by credit providers or anyone else who has your permission.</li>
<li>Not to charge for corrections or challenges to the information by you, the consumer.</li>
<li>Not to make a negative judgement about you when they do not have any credit information.</li>
</ul>
<b class="b_heading">How can your credit information be used?</b>
<ul>
<li>To decide whether or not you can afford credit.</li>
<li>To investigate fraud, corruption or theft.</li>
<li>To consider you for employment in a position that requires trust, honesty and the handling of cash or finances.</li>
</ul>
<b class="b_heading">What are your rights regarding a Credit Bureau?</b>
<ul>
<li>To be told that a credit provider intends to report negative information on you to a credit bureau 20 working days before they do so.</li>
<li>To get a copy of your credit record from a credit bureau when you ask for it – you can get one free record each year but may be charged a small fee for further records.</li>
<li>To challenge information kept by a credit bureau if you are unhappy with it.</li>
<li>For your information to be kept confidential, and for it to be used only for the purposes that are allowed.</li>
</ul>
<b class="b_heading">How do you apply for your free Credit Record?</b>
<ul>
