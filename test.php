<div class="panel-heading">
<h4 class="panel-title">
<a href="#mp14" data-toggle="collapse" data-parent="#mp" onclick="display_accord('#mp3');">The National Credit Act (No 34 of 2005) (NCA)</a>
</h4>
</div>
<div class="panel-collapse collapse" id="mp3">
<p class="panel-body">
The National Credit Act (No 34 of 2005) (NCA) which came into effect on 1 June 2007, recognizes that there are times when people need to borrow money in order to buy certain things. In other words, they need to get credit from credit providers.
The NCA provides a framework for every type of credit transaction, including microloans, homeloans, bank overdrafts and furniture finance. The NCA impacts on consumers, credit bureaux and credit providers – ranging from microlenders to banks.
The NCA sets out the rights and duties of both consumers and credit providers and includes measures that allow consumers to make informed decisions before procuring credit. It also places a responsibility on credit providers to ensure that they do not advance credit when consumers cannot afford it. The NCA also regulates the way in which credit bureaux conduct their businesses.
The NCA replaced the Usury Act (which governed moneylending transactions), including the Exemption Notice to the Usury Act which governed micro-lending and the Credit Agreements Act (which governed instalment sale or hire purchase agreements).
The National Credit Regulator (NCR) was established under the NCA and is responsible for enforcing the provisions of the NCA. The NCR is also responsible for educating consumers about their rights under the NCA.
<b class="b_heading">Purpose of the NCA</b>
The NCA aims to create a fair non-discriminatory environment in which people borrow and lend money while at the same time guarding against people being given a loan when they cannot afford to pay it back.
The NCA refers to people who borrow as consumers and says that they should be assisted and protected in the following ways. They should be:
<ul>
<li>provided with information in a language they understand so that they may make informed decisions about borrowing money,</li>
<li>protected from getting into too much debt,</li>
<li>assured that credit providers follow the law when providing services to them,</li>
<li>assured that their personal information will remain private (confidential), and given help with the management of their debt.</li>
</ul>
<strong>The purposes of the NCA are:</strong>
To promote and advance the social and economic welfare of South Africans, promote a fair, transparent, competitive, sustainable, responsible, efficient, effective and accessible credit market and industry, and to protect consumers, by:
<ol type="a">
<li>promoting the development of a credit market that is accessible to all South Africans.</li>
<li>ensuring consistent treatment of different credit products and different credit providers.</li>
<li>promoting responsibility in the credit market by:
<ul>
<li>encouraging responsible borrowing, avoiding over-indebtedness,&nbsp; and</li>
<li>discouraging reckless credit granting by credit providers and contractual default by consumers.</li>
</ul>
</li>
<li>promoting equity in the credit market by balancing the respective rights and responsibilities of credit providers and consumers;</li>
<li>addressing and correcting imbalances in negotiating power between consumers and credit providers by-
<ul>
<li>providing consumers with education about credit and consumer rights.</li>
<li>providing consumers with adequate information of standardised information in order to make informed choices, and</li>
<li>providing consumers with protection from deception, and from unfair or fraudulent conduct by credit providers and credit bureaus.</li>
</ul>
</li>
<li>improving consumer credit information and reporting and regulation of credit bureaux.</li>
<li>addressing and preventing over-indebtedness of consumers, and providing mechanisms for resolving over-indebtedness.</li>
<li>providing for an accessible system for resolving disputes arising from credit agreements, and</li>
<li>providing for a system of debt restructuring, enforcement and judgment, which places priority on the eventual satisfaction of all responsible consumer obligations under credit agreements.</li>
</ol>
You can get more information and downloads about the National Credit Act at&nbsp;<a href="http://www.ncr.org.za/" target="_blank"> www.ncr.org.za</a>
